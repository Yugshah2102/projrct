// @desc    Get user stats
// @route   GET /api/auth/stats
// @access  Private
router.get('/stats', protect, async (req, res) => {
  try {
    const userId = req.user._id;

    // Get user's items count
    const Item = require('../models/Item');
    const SwapRequest = require('../models/SwapRequest');

    const totalItemsUploaded = await Item.countDocuments({ uploaderId: userId });
    const totalSwapsCompleted = await SwapRequest.countDocuments({
      $or: [
        { requesterId: userId },
        { itemOwnerId: userId }
      ],
      status: 'completed'
    });

    const stats = {
      totalItemsUploaded,
      totalSwapsCompleted,
      totalPointsEarned: req.user.points, // This could be calculated from transaction history
      totalPointsSpent: 0, // This could be calculated from transaction history
      currentPoints: req.user.points,
      memberSince: req.user.joinedAt
    };

    res.json({
      success: true,
      data: stats,
      message: 'User stats retrieved successfully'
    });
  } catch (error) {
    console.error('Stats error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error'
    });
  }
});

